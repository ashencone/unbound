int get_pokemon_index(char *name)
{
    for (int i = 0; i < NUM_SPECIES - 1; i++)
    {
        int cmp = strcmp(name, POKEMON_SPECIES_NAMES[i]);

        if (cmp == 0)
        {
            return i;
        }
    }
    return -1;
}